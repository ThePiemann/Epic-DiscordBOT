const SHOP_TOOLS = TOOLS.filter(tool => tool.buyable);

const getToolById = (id) => TOOLS.find(t => t.id === id);

// Helper to find the best tool in user's inventory
const getBestTool = (inventory, type) => {
    // Filter tools the user actually HAS
    const userTools = TOOLS.filter(tool => 
        tool.type === type && 
        inventory.get(tool.id) && 
        inventory.get(tool.id) > 0
    );

    // Sort by efficiency (descending)
    if (userTools.length === 0) return null;
    return userTools.sort((a, b) => b.efficiency - a.efficiency)[0];
};

